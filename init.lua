-- 2) helper to find the next non‚Äêblank line number
function _G.next_non_blank_line(lnum)
  local last = fn.line '$'
  for i = lnum + 1, last do
    if fn.getline(i):match '%S' then
      return i
    end
  end
  return -2
end

-- 3) the fold‚Äêexpression function
function _G.get_potion_fold(lnum)
  local line = fn.getline(lnum)
  -- if blank line, never start a fold
  if line:match '^%s*$' then
    return '-1'
  end

  -- compute indent‚Äêlevel in 'shiftwidth' units
  local indent = fn.indent(lnum) / vim.o.shiftwidth
  local ft = vim.bo.filetype

  -- special: in TS/TSX, bump indent on <div>, unbump on </div>
  if ft == 'typescript' or ft == 'typescriptreact' then
    if line:match '<div[^>]*>' then
      return '>' .. tostring(indent + 1)
    end
    if line:match '</div>' then
      return tostring(indent)
    end
  end

  -- otherwise fall back to plain indent folding
  local nextln = next_non_blank_line(lnum)
  local next_indent = (nextln > 0) and (fn.indent(nextln) / vim.o.shiftwidth) or indent

  if next_indent <= indent then
    return tostring(indent)
  else
    return '>' .. tostring(next_indent)
  end
end

-- 4) custom text to show in the folded line
function _G.custom_fold_text()
  -- find first non‚Äêblank in the fold
  local fs = vim.v.foldstart
  while fs <= vim.v.foldend and fn.getline(fs):match '^%s*$' do
    fs = fn.nextnonblank(fs + 1)
    if fs == 0 then
      break
    end
  end

  local line
  if fs > vim.v.foldend then
    line = fn.getline(vim.v.foldstart)
  else
    -- replace tabs with spaces so strwidth() is correct
    line = fn.getline(fs):gsub('\t', string.rep(' ', vim.o.tabstop))
  end

  -- compute how much room we have
  local winw = api.nvim_win_get_width(0)
  local colw = vim.o.foldcolumn
  local numw = vim.o.number and 8 or 0
  local avail = winw - colw - numw

  -- the " ‚Ä¶ N lines +--+-- " suffix
  local foldSize = vim.v.foldend - vim.v.foldstart + 1
  local size_str = ' ' .. foldSize .. ' lines '
  local level_str = string.rep('+--', vim.v.foldlevel)
  local suffix = size_str .. level_str

  -- pad in between
  local padding = avail - fn.strwidth(suffix)
  if padding < 0 then
    padding = 0
  end
  local padstr = string.rep(' ', padding)

  return line .. padstr .. suffix
end

